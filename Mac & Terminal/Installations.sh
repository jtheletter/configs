# Download latest Node/NPM (https://nodejs.org/).

# Install Brew:
# Follow instruction on - https://brew.sh/
# Or update Brew:
brew update

# Install Ruby:
# brew install ruby
# Or upgrade Ruby:
brew upgrade ruby

